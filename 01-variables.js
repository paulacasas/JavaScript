// Las variables en js no se declaran 
// Lower camel case para las variables

// var (forma original para declarar variables)
var helloWorld = "Hello, JavaScript";
console.log(helloWorld);

helloWorld = "Hola de nuevo, JavaScript";
console.log(helloWorld);


// let (utilizada)
let helloWorld2 = "Hello, JavaScript 2";
console.log(helloWorld2);

helloWorld2 = "Hola de nuevo, JavaScript 2";
console.log(helloWorld2);

// const (no se puede "modificar")
const helloWorld3 = "Hello";
console.log(helloWorld3);

// intentar cambiar el valor de una const
helloWorld3 = "Hello, JavaScript";
console.log(helloWorld3);